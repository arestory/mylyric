package com.ares.entity.stock;

public class Stock925 {


    private String id;
    private String name;
    private long callAuction;
    private String createTime;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public long getCallAuction() {
        return callAuction;
    }

    public void setCallAuction(long callAuction) {
        this.callAuction = callAuction;
    }

    public String getCreateTime() {
        return createTime;
    }

    public void setCreateTime(String createTime) {
        this.createTime = createTime;
    }
}
